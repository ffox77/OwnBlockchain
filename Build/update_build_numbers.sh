BUILD_MAJOR=0
BUILD_MINOR=2
BUILD_NUMBER=0
BASE_REVISION=0

ASSEMBLY_FILE_PATH=$1
REVISION_NUMBER=$2

VERSION_TEMPLATE="(\"%s.%s.%s.%s\")"
INFORMATIONAL_VERSION_TEMPLATE="(\"%s.%s.%s\")"

VERSION_TO_REPLACE=`printf "$VERSION_TEMPLATE" $BUILD_MAJOR $BUILD_MINOR $BUILD_NUMBER $BASE_REVISION`
REPLACING_VERSION=`printf "$VERSION_TEMPLATE" $BUILD_MAJOR $BUILD_MINOR $BUILD_NUMBER $REVISION_NUMBER`

sed -i "s/$VERSION_TO_REPLACE/$REPLACING_VERSION/g" "$ASSEMBLY_FILE_PATH"

